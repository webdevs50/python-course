## Defining a module
## We use modules to orgnize our code
## Instead of writing our code in one file, we can break our code to be in multiple files
## Each file is called a module
## Each Module should contain all the releated functions and classes
## We can import a module to be used in another module

def lbs_to_kg(weight):
    return weight * 0.45

def kg_to_lbs(weight):
    return weight / 0.45