## LESSON 5.2 Working with modules
## For complex mathematical operations we need to import and use Python modules which define 
## more complex functions
import math
print(math.ceil(2.1))  ## Ceiling method

print(math.floor(3.7))  ## flooring method

print(math.factorial(30)) ## getting the factorial value of a number